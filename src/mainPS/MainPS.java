    void comprobarUsuario() {
        String user = userField.getText().toLowerCase();
        String pass = passField.getText().toLowerCase();
        if ((user.equals("admin")) && pass.equals("admin")) {
            panel_ajustes.setVisible(true);
            panel_login.setVisible(false);
            resetUserPassColor();
        } else {
            UserPassColor();
        }
    }

