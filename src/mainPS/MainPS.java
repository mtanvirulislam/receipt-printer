    void UserPassColor() {
        user_panel.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, Color.RED));
        userField.setForeground(Color.red);
        pass_panel.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, Color.RED));
        passField.setForeground(Color.red);
    }

    void resetUserPassColor() {
        userField.setText("Usuario");
        passField.setText("password");
        user_panel.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(54, 33, 89)));
        userField.setForeground(new Color(54, 33, 89));
        pass_panel.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(54, 33, 89)));
        passField.setForeground(new Color(54, 33, 89));
    }

