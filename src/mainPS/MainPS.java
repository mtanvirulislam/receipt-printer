    private void btn_add_movilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_add_movilMouseClicked
        if (!(marca_movil_venta.getText().equals("")) && !(modelo_movil_venta.getText().equals(""))
                && !(imei1_movil_venta.getText().equals("")) && !(precio_movil_venta.getText().equals(""))) {
            Metodos.tablaMovil(marca_movil_venta, modelo_movil_venta, imei1_movil_venta, imei2_movil_venta, precio_movil_venta, tabla_movil_venta);
            Metodos.resetVentaMovil(marca_movil_venta, modelo_movil_venta, imei1_movil_venta, imei2_movil_venta, precio_movil_venta);
        } else {
            //JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacíos");
            JOptionPane.showMessageDialog(null,"Los campos no pueden estar vacíos" , "", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_add_movilMouseClicked

